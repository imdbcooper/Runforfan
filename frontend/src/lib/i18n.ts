let runtimeLanguage: Language = "ru"
const SKIP_SELECTOR = '[data-i18n-skip], [translate="no"]'
const UI_SELECTOR = '[data-i18n-ui], button, label, option, summary, th, nav, header'

function safeGetLanguage(): Language {
  try {
    const stored = localStorage.getItem(LANGUAGE_KEY)
    return stored === "ru" || stored === "en" ? stored : "ru"
  } catch {
    return "ru"
  }
}

export function getInitialLanguage() {
  runtimeLanguage = safeGetLanguage()
  return runtimeLanguage
}

export function saveLanguage(language: Language) {
  runtimeLanguage = language
  try {
    localStorage.setItem(LANGUAGE_KEY, language)
  } catch {
    // Language switching still works for the current session.
  }
}

export function languageLocale() {
  return runtimeLanguage === "en" ? "en-US" : "ru-RU"
}

function preserveWhitespace(source: string, translated: string) {
  const leading = source.match(/^\s*/)?.[0] || ""
  const trailing = source.match(/\s*$/)?.[0] || ""
  return `${leading}${translated}${trailing}`
}

function isUiContext(element: Element) {
  if (element.closest(UI_SELECTOR)) return true
  const className = typeof element.className === "string" ? element.className : ""
  if (className.includes("font-mono") && className.includes("uppercase")) return true
  if ((className.includes("font-semibold") || className.includes("font-medium")) && className.includes("text-white")) return true
  return /^H[1-6]$/.test(element.tagName)
}

function isGuardedPhrase(source: string, translated: string) {
  const trimmed = source.trim()
  const next = translated.trim()
  const wordCount = trimmed.split(/\s+/).filter(Boolean).length
  const hasSentencePunctuation = /[.!?,:;]/.test(trimmed)
  const tokenLike = /^[\p{L}\p{N}_/ &+-]+$/u.test(trimmed)
  const shortStatusLike = tokenLike && wordCount <= 3 && trimmed.length <= 28
  const casingChanged = trimmed.toLocaleLowerCase() !== next.toLocaleLowerCase()
  return shortStatusLike && (!hasSentencePunctuation || casingChanged)
}

export function translateText(source: string, language: Language, options: { uiContext?: boolean } = {}) {
  if (!source.trim()) return source
  const trimmed = source.trim()
  const exact = language === "ru" ? TO_RU.get(trimmed) || RU_ALIASES.get(trimmed) : TO_EN.get(trimmed) || EN_ALIASES.get(trimmed)
  if (exact) {
    if (!options.uiContext && isGuardedPhrase(trimmed, exact)) return source
    return preserveWhitespace(source, exact)
  }
  if (!options.uiContext) return source
  let next = trimmed
  for (const [pattern, replacement] of REPLACEMENTS[language]) next = next.replace(pattern, replacement)
  return preserveWhitespace(source, next)
}

function shouldSkipElement(element: Element) {
  if (["SCRIPT", "STYLE", "TEXTAREA", "CODE", "PRE", "SVG"].includes(element.tagName)) return true
  if (element.closest(SKIP_SELECTOR)) return true
  const tableBody = element.closest("tbody")
  if (tableBody && !element.closest("button, label, option, summary, th")) return true
  return false
}

function shouldSkipTextNode(node: Text) {
  return !node.parentElement || shouldSkipElement(node.parentElement)
}

function translateTextNode(node: Text, language: Language) {
  if (shouldSkipTextNode(node)) return
  const current = node.nodeValue || ""
  const next = translateText(current, language, { uiContext: isUiContext(node.parentElement as Element) })
  if (current !== next) node.nodeValue = next
}

function translateElementAttributes(element: Element, language: Language) {
  if (shouldSkipElement(element)) return
  for (const attribute of ATTRIBUTES) {
    const current = element.getAttribute(attribute)
    if (current === null) continue
    const next = translateText(current, language, { uiContext: isUiContext(element) || attribute === "placeholder" })
    if (current !== next) element.setAttribute(attribute, next)
  }
}

function translateTree(root: Node, language: Language) {
  if (root.nodeType === Node.TEXT_NODE) translateTextNode(root as Text, language)
  if (root.nodeType !== Node.ELEMENT_NODE && root.nodeType !== Node.DOCUMENT_NODE) return
  const element = root as Element
  if (root.nodeType === Node.ELEMENT_NODE) translateElementAttributes(element, language)
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT | NodeFilter.SHOW_ELEMENT)
  let node = walker.nextNode()
  while (node) {
    if (node.nodeType === Node.TEXT_NODE) translateTextNode(node as Text, language)
    else if (node.nodeType === Node.ELEMENT_NODE) translateElementAttributes(node as Element, language)
    node = walker.nextNode()
  }
}

export function useDomTranslations(language: Language) {
  useEffect(() => {
    document.documentElement.lang = language
    translateTree(document.body, language)
    const observer = new MutationObserver((mutations) => {
      for (const mutation of mutations) {
        if (mutation.type === "characterData") translateTree(mutation.target, language)
        for (const node of mutation.addedNodes) translateTree(node, language)
        if (mutation.type === "attributes") translateTree(mutation.target, language)
      }
    })
    observer.observe(document.body, { childList: true, characterData: true, subtree: true, attributes: true, attributeFilter: ATTRIBUTES })
    return () => observer.disconnect()
  }, [language])
}
